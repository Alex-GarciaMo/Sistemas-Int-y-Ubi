let arr = [ 1, 2, 15, 5 ];
// sort reordena el contenido del Array 
arr.sort();
console.log(arr);
//Los elementos se ordenan, de forma predeterminada, como si fueran cadenas de carácteres.
//Literalmente, todos los elementos se convierten en cadenas de carácteres y luego se comparan.
//Por lo tanto, se aplica el ordenamiento lexicográfico y, de hecho, "2" > "15".